// Different types of Integer Literals
public class IntegerLiterals {
    public static void main(String[] args) {
        int a=10;
        int b=0b10111;
        int c=0123;
        int d=0xF;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
